    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    Conectadb cc = new Conectadb();
    Connection cn = cc.Conectar();
    String cedu;
    String nom;
    String ape;
    String dir;
    boolean modop;
    String ciu;
    String ema;
    String tel;
    String sql="";
    cedu = cedula.getText();
    int nce = Integer.parseInt(cedu);
    nom = nombre.getText();
    ape = apellido.getText();
    dir = direccion.getText();
    modop = button1.isSelected();
    ciu = ciudad.getText();
    ema = email.getText();
    tel = telefono.getText();
    int tele = Integer.parseInt(tel);
    
    sql="INSERT INTO clientes (cedula, nombre, apellido, direccion, modo_pago, ciudad, email, telefono) VALUES (?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);
            psd.setInt(1, nce);
            psd.setString(2, nom);
            psd.setString(3, ape);
            psd.setString(4, dir);
            psd.setBoolean(5, modop);
            psd.setString(6, ciu);
            psd.setString(7, ema);
            psd.setInt(8, tele);
            
            
            int n=psd.executeUpdate();
            if (n>0){
                JOptionPane.showMessageDialog(null,"Registro De Cliente Guardado Con Exito");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cliente_registro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

