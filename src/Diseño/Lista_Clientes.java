    void cargar(String valor){
        String [] titulos={"Nombre","Apellido","Cedula","Direccion","Modo De Pago","Ciudad","Email","Telefono"};
        String [] registros = new String[8];
        String sql;
        if(valor.equals("")){
            sql="SELECT * FROM clientes";
        }else{
            sql="SELECT * FROM clientes WHERE nombre LIKE '%"+valor+"%'";
        }
        
        model = new DefaultTableModel(null,titulos);
        
        Conectadb cc = new Conectadb();
        Connection cn = cc.Conectar();
        
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                registros[0]=rs.getString("nombre");
                registros[1]=rs.getString("apellido");
                registros[2]=rs.getString("cedula");
                registros[3]=rs.getString("direccion");
                registros[4]=rs.getString("modo_pago");
                registros[5]=rs.getString("ciudad");
                registros[6]=rs.getString("email");
                registros[7]=rs.getString("telefono");
                model.addRow(registros);
            }
            tabla3.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(Lista_Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
